# 5. Faça um Programa que converta metros para centímetros. 
# Você pode pedir o comprimento em metros para o usuário (input).

metro= int(input('Digite quantos metros deseja converter para centímetros: '))

centimetros= metro*100
print(f'A conversão de {metro} metros para centímetros equivale há {centimetros} centímetros.')